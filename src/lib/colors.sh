#!/bin/bash
# Shared ANSI color definitions for Franklin scripts.

if [ -n "${FRANKLIN_COLORS_LOADED:-}" ]; then
  return 0 2>/dev/null || true
fi

franklin_use_truecolor() {
  if [ "${FRANKLIN_FORCE_TRUECOLOR:-0}" -eq 1 ]; then
    return 0
  fi
  if [ "${FRANKLIN_DISABLE_TRUECOLOR:-0}" -eq 1 ]; then
    return 1
  fi
  case "${COLORTERM:-}" in
    *truecolor*|*24bit*) return 0 ;;
  esac
  return 1
}

